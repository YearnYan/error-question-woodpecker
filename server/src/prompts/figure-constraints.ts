/**
 * Subject-specific SVG figure rendering constraints.
 * Distilled from the constraint specification documents in /constraints/*.md
 * These are injected into the renderer prompt to ensure accurate figure generation.
 */

export interface FigureConstraints {
  /** Subject-specific hard rules the SVG must satisfy */
  hardRules: string
  /** Common mistakes to explicitly avoid */
  commonMistakes: string
  /** SVG technical requirements specific to this subject */
  svgTechnical: string
}

