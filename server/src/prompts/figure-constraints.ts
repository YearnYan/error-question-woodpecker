/**
 * Get subject-specific figure rendering constraints
 */
export function getFigureConstraints(subject: string): FigureConstraints {
  const constraintsMap: Record<string, FigureConstraints> = {
    '数学': MATH_CONSTRAINTS,
    '物理': PHYSICS_CONSTRAINTS,
    '化学': CHEMISTRY_CONSTRAINTS,
    '生物': BIOLOGY_CONSTRAINTS,
    '地理': GEOGRAPHY_CONSTRAINTS,
    '英语': ENGLISH_CONSTRAINTS,
    '政治': POLITICS_CONSTRAINTS,
    '历史': HISTORY_CONSTRAINTS,
    '语文': CHINESE_CONSTRAINTS,
  }

  return constraintsMap[subject] || MATH_CONSTRAINTS
}

