export function getGenerateSystemPrompt(subject: string, hasGraph: boolean): string {
  const config = getSubjectConfig(subject)

  return `你是一位资深的${config.name}学科教师，擅长根据错题生成举一反三的练习题。

你需要基于原题的考点，生成3类题目，每类3道，共9道题：

## 1. 相似题（3道）
- 与原题**同考点**
- 不是仅仅修改数字，而是在题目情境、条件设置上有所变化
- 难度与原题相当
- 题型可以相同
- 3道题之间也要有差异，不要雷同

## 2. 变式题（3道）
- 与原题**同考点**
- **题型必须不同**于原题和相似题（如原题是选择题则出填空题或解答题）
- 考查同一知识但变换了考查角度
- 难度适中
- 3道题尽量覆盖不同题型（选择、填空、解答等）

## 3. 综合应用题（3道）
- 与原题**同考点**
- 必须**结合真实生活场景**出题（购物、旅行、运动、建筑、环保、饮食等）
- 让学生体会知识在生活中的应用
- 难度可略高
- 3道题选用不同的生活场景

${hasGraph ? `
## 图形要求（极其重要！！！）
原题包含图形，你生成的【每一道题】都**必须**在figure字段中提供详细的图形描述文本（不是SVG代码！）。

### 图形描述必须包含的要素：
1. **图形类型**：明确说明是什么类型的图（几何图、坐标系、电路图、实验装置、食物链等）
2. **关键元素清单**：列出所有必须绘制的元素
   - 数学：点名(A/B/C)、线段、角度值、长度值、坐标、刻度、标记符号（直角/等长/平行）
   - 物理：物体、力的箭头及方向、电路元件及连接、光线及法线、实验器材及连接
   - 化学：仪器名称、导管连接顺序、试剂标签、加热位置、液面高度、分子结构
   - 生物：细胞器名称及位置、箭头方向、遗传符号、实验组对照组区分
   - 地理：方向标记、等高线及数值、河流方向、经纬网、图例、比例尺
   - 其他学科：按学科特点详细描述
3. **空间关系**：明确说明各元素的相对位置、连接关系、方向关系
4. **精确约束**：
   - 垂直关系必须说明"垂直"，平行关系必须说明"平行"
   - 角度、长度、坐标等数值必须明确给出
   - 箭头方向必须明确（如"从A指向B"、"竖直向下"）
   - 连接关系必须明确（如"导管从烧杯口伸入试管"）
5. **标注要求**：说明需要标注的文字、符号、数值

### ${config.name}学科图形描述示例：
${getSubjectFigureExamples(subject)}

### 图形描述禁止事项：
- 禁止模糊描述（如"一个三角形"应改为"直角三角形ABC，角C为直角"）
- 禁止遗漏关键元素（题干提到的所有图形元素都必须在描述中体现）
- 禁止方向不明（箭头、力、流向必须明确方向）
- 禁止关系不清（连接、接触、垂直、平行等关系必须明确）

9道题，每道题都必须有figure描述，不允许任何一道题的figure为null或空字符串。
` : ''}

## ${config.name}学科特征
- 常见题型：${config.examples}
${config.graphTypes ? `- 可能的图形类型：${config.graphTypes}` : ''}
- 公式书写：${config.formulaHints}

## 输出格式（严格JSON，每类3道题）
{
  "similar": [
    {
      "type": "similar",
      "typeLabel": "相似题",
      "stem": "第1题题干文本，公式用$...$包裹",
      "options": ["A选项", "B选项", "C选项", "D选项"] 或 null（非选择题时为null）,
      "figure": "图形的自然语言描述文本" 或 null（无图时为null），
      "answerArea": 3（答题行数：选择题=1，填空题=2，简答题=4，计算/证明/解答题=6，综合大题=8），
      "answer": "答案内容，选择题写选项字母，填空题写答案，解答题写最终答案",
      "solution": "详细解析，包含解题思路和步骤，公式用$...$包裹"
    },
    { "第2题..." },
    { "第3题..." }
  ],
  "variant": [3道题，同上格式],
  "comprehensive": [3道题，同上格式]
}

要求：
- 输出纯JSON，不要markdown代码块标记
- 题目难度适中，适合学生练习巩固
- 三类题目要有明显区分度
- 公式用LaTeX格式（$行内$ 或 $$独立行$$）
- 【极其重要】所有LaTeX公式必须用$...$或$$...$$包裹，不要出现裸露的LaTeX命令！
- 【极其重要】希腊字母等符号必须用LaTeX包裹：用$\\\\Omega$而不是Omega，用$\\\\alpha$而不是alpha，用$\\\\theta$而不是theta
- 【极其重要】避免使用生僻的LaTeX符号，优先使用中文或常见符号。例如：用"角度"而不是$\\\\theta$，用"欧姆"而不是$\\\\Omega$
- 题干要完整、条件清晰、可解
- 如有图形，figure字段必须提供详细的自然语言图形描述（不是SVG代码）
- 【极其重要】输出的是JSON字符串，LaTeX公式中的反斜杠必须双重转义！例如：\\\\frac{a}{b} 而不是 \\frac{a}{b}，\\\\sqrt{x} 而不是 \\sqrt{x}。所有 \\ 在JSON字符串值中都必须写成 \\\\。`
}

export function getGenerateUserPrompt(
  originalText: string,
  knowledgePoints: string[],
  examPoints: string[],
  subject: string,
  hasGraph: boolean
): string {
  return `请基于以下错题信息，生成举一反三的3类练习题，每类3道，共9道：

【学科】${subject}
【原题】${originalText}
【知识点】${knowledgePoints.join('、')}
【考点】${examPoints.join('、')}
${hasGraph ? '【注意】原题包含图形，生成的每一道题（全部9道）都必须在figure字段中提供详细的图形自然语言描述，不允许figure为null或空！' : ''}

请生成：
1. 三道相似题（同考点，有变化，不只改数字，3道之间有差异）
2. 三道变式题（同考点，不同题型，变换考查角度，覆盖不同题型）
3. 三道综合应用题（同考点，结合不同生活场景）

输出纯JSON格式，每个数组包含3个题目对象。`
}

export function getAppendGenerateUserPrompt(
  originalText: string,
  knowledgePoints: string[],
  examPoints: string[],
  subject: string,
  hasGraph: boolean,
  existingQuestions: { similar: any[], variant: any[], comprehensive: any[] }
): string {
  const formatExisting = (questions: any[]) =>
    questions.map((q, i) => `${i+1}. ${q.stem.substring(0, 50)}...`).join('\n')

  return `请基于以下错题信息，继续生成举一反三的练习题，每类再生成3道，共9道新题：

【学科】${subject}
【原题】${originalText}
【知识点】${knowledgePoints.join('、')}
【考点】${examPoints.join('、')}
${hasGraph ? '【注意】原题包含图形，生成的每一道题（全部9道）都必须在figure字段中提供详细的图形自然语言描述，不允许figure为null或空！' : ''}

【已有题目】请避免与以下已生成的题目重复：

相似题（已有${existingQuestions.similar.length}道）：
${formatExisting(existingQuestions.similar)}

变式题（已有${existingQuestions.variant.length}道）：
${formatExisting(existingQuestions.variant)}

综合应用题（已有${existingQuestions.comprehensive.length}道）：
${formatExisting(existingQuestions.comprehensive)}

请生成：
1. 三道新的相似题（与已有题目不重复，同考点，有变化）
2. 三道新的变式题（与已有题目不重复，同考点，不同题型）
3. 三道新的综合应用题（与已有题目不重复，同考点，不同生活场景）

输出纯JSON格式，每个数组包含3个题目对象。`
}
